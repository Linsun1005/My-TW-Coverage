count = 0
for root, dirs, files in os.walk(reports_dir):
    for file in files:
        if file.endswith('.md'):
            ticker = file[:4]
            if ticker in data:
                fpath = os.path.join(root, file)
                with open(fpath, 'r', encoding='utf-8') as f:
                    content = f.read()
                
                # Extract metadata
                metadata_lines = []
                for line in content.split('\n'):
                    if '板塊:' in line or '產業:' in line or '市值:' in line or '企業價值:' in line:
                        metadata_lines.append(line.strip())
                metadata_str = '\n'.join(metadata_lines)

                # Extract financial section entirely
                fin_idx = content.find('## 財務概況')
                fin_section = content[fin_idx:] if fin_idx != -1 else ''

                d = data[ticker]
                
                new_content = f'''# {file.replace('.md', '')}

## 業務簡介
{metadata_str}

{d['desc']}

## 供應鏈位置
*   **上游**: {d['up']}
*   **中游**: {d['mid']}
*   **下游**: {d['down']}

## 主要客戶及供應商
### 主要客戶
*   {d['cust']}

### 主要供應商
*   {d['supp']}

{fin_section}'''

                with open(fpath, 'w', encoding='utf-8') as f:
                    f.write(new_content)
                print(f'Successfully fixed and updated missing ticker {ticker}')
                count += 1
print(f'Fixed {count} files.')
